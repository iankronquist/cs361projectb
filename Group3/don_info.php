	<h3>General Health Considerations</h3>
	<pre>
	<b>Allergy, Stuffy Nose, Itchy Eyes, Dry Cough</b>
	Acceptable as long as you feel well, have no fever, and have no problems breathing through your mouth.
	<b>Cold, Flu</b>
	Wait if you have a fever or a productive cough (bringing up phlegm)
	Wait if you do not feel well on the day of donation.
	Wait until you have completed antibiotic treatment for sinus, throat or lung infection.	
	<b>Donation Intervals</b>
	Wait at least 8 weeks between whole blood (standard) donations.
	Wait at least 7 days between platelet (pheresis) donations.
	Wait at least 16 weeks between double red cell (automated) donations.	
	<b>Weight/Height</b>
	You must weigh at least 110 lbs to be eligible for blood donation for your own safety.
	Students who donate at high school drives and donors 18 years of age or younger must also meet additional height and weight 
	requirements for whole blood donation 
	(applies to girls shorter than 5'6" and boys shorter than 5').
	Blood volume is determined by body weight and height. Individuals with low blood volumes may not tolerate the removal of the required 
	volume of blood given with whole blood donation. 
	There is no upper weight limit as long as your weight is not higher than the weight limit of the donor bed/lounge you are using. 
	You can discuss any upper weight limitations of beds and lounges with your local health historian.
