	<h1> Donation eligibility</h1>
	<h2> Medications and vaccinations</h2>
	
	<h3> Antibiotics </h3> 
	<p> A donor with an acute infection should not donate. The reason for antibiotic use must be evaluated to determine if the donor has a bacterial infection that could be transmissible by blood.
Acceptable after finishing oral antibiotics for an infection (bacterial or viral). May have taken last pill on the date of donation. Antibiotic by injection for an infection acceptable 10 days after last injection. Acceptable if you are taking antibiotics to prevent an infection, for example, following dental procedures or for acne. Some conditions which require antibiotics to prevent an infection must still be evaluated at the time of donation by the responsible medical director. If you have a temperature above 99.5 F, you may not donate.</p>
	
	<h3>Birth Control</h3>
	<p>Women on oral contraceptives or using other forms of birth control are eligible to donate.</p>

